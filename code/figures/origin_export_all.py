o.Execute(r'layer.x.label.text$ = "Model"; layer.y.label.text$ = "Value";')
o.Execute(f'expGraph type:=pdf filename:="{OUT}\\fig_models_origin.pdf" tr:=origin;')
o.Execute(f'expGraph type:=png filename:="{OUT}\\fig_models_origin.png" width:=1200 tr:=origin;')
print("  [3/5] Models done")
time.sleep(1)

# === Figure 4: Cross-domain transfer ===
o.Execute("doc -n;")
o.Execute("wks.ncols = 4;")
o.Execute(r'wks.col1.label$ = "Arch"; wks.col2.label$ = "CWRU"; wks.col3.label$ = "XJTU-SY"; wks.col4.label$ = "Z3 Rate";')
o.Execute("wks.col1.text$ = B-spline; wks.col2.cell1$ = 99.93; wks.col3.cell1$ = 91.7; wks.col4.cell1$ = 100;")
