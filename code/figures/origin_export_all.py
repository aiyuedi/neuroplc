o.Execute(r'layer.x.label.text$ = "Architecture"; layer.y.label.text$ = "Value (%)";')
o.Execute(f'expGraph type:=pdf filename:="{OUT}\\fig_cross_domain_origin.pdf" tr:=origin;')
o.Execute(f'expGraph type:=png filename:="{OUT}\\fig_cross_domain_origin.png" width:=1200 tr:=origin;')
print("  [4/5] Cross-domain done")
time.sleep(1)

# === Figure 5: WCET pie ===
o.Execute("doc -n;")
o.Execute("wks.ncols = 2;")
o.Execute(r'wks.col1.label$ = "Component"; wks.col2.label$ = "Time (ms)";')
o.Execute("wks.col1.text$ = LUT L0 (448 edges); wks.col2.cell1$ = 16.44;")
