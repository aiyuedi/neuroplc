    f.write("   d=h\n")
    f.write("  layers.append(nn.Linear(d,num_classes))\n")
    f.write("  self.net=nn.Sequential(*layers)\n")
    f.write("  for m in self.modules():\n")
    f.write("   if isinstance(m,nn.Linear):\n")
    f.write("    nn.init.kaiming_uniform_(m.weight,nonlinearity='relu')\n")
    f.write("    nn.init.zeros_(m.bias)\n")
    f.write(" def forward(self,x): return self.net(x)\n")
