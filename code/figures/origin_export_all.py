o.Execute(f'expGraph type:=pdf filename:="{OUT}\\fig_wcet_origin.pdf" tr:=origin;')
o.Execute(f'expGraph type:=png filename:="{OUT}\\fig_wcet_origin.png" width:=1200 tr:=origin;')
print("  [5/5] WCET done")

print(f"\nAll 5 Origin figures exported to {OUT}")
